package FichaPratica03;

public class Incrementos {
    public static void main(String[] args) {

        int num = 1;


        num++;      // num = num + 1;

        num += 10;  // num = num + 10;


        num--;      // num = num -1

        num -= 10;  // num = num -10


        num *= 10;  // num = num * 10;
        num /= 10;  // num = num / 10;
        num %= 10;  // num = num % 10;
    }
}
